const I18nContext = createContext();

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(getSettings().language || 'zh');

  useEffect(() => {
    const handleSettingsChanged = () => {
      const newLang = getSettings().language || 'zh';
      if (newLang !== lang) {
        setLang(newLang);
      }
    };

    window.addEventListener('settings_changed', handleSettingsChanged);
    return () => window.removeEventListener('settings_changed', handleSettingsChanged);
  }, [lang]);

  const t = useCallback((key) => {
    const keys = key.split('.');
    let value = resources[lang];
    for (const k of keys) {
      if (value === undefined) break;
      value = value[k];
    }
    return value || key;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useTranslation() {
  return useContext(I18nContext);
}
